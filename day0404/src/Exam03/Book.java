package Exam03;

public class Book {
	private String title;
	private String author;
	
	//setter
	public void setTitle(String t) {
		title = t;
	}
	public void setAuthor(String a) {
		author = a;
		
	}
	
	//getter
	public String getTitle() {
		return title;
	}
	public String getAuthor() {
		return author;
	}
}
